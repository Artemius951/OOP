package ru.nsu.kutsenko.task221;

/**
 * Состояния жизненного цикла заказа пиццы.
 */
public enum OrderStatus {

    /** Заказ только создан и ожидает в очереди. */
    NEW,

    /** Заказ готовится пекарем. */
    COOKING,

    /** Заказ готов и ожидает в хранилище. */
    READY,

    /** Заказ доставляется курьером. */
    DELIVERING,

    /** Заказ успешно доставлен. */
    DELIVERED,

    /** Заказ отменен. */
    CANCELLED
}