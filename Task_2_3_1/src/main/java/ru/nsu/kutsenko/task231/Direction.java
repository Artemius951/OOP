package ru.nsu.kutsenko.task231;

/**
 * Перечисление возможных направлений движения.
 */
public enum Direction {
    /**
     * Движение вверх (отрицательное направление Y).
     */
    UP(0, -1),

    /**
     * Движение вниз (положительное направление Y).
     */
    DOWN(0, 1),

    /**
     * Движение влево (отрицательное направление X).
     */
    LEFT(-1, 0),

    /**
     * Движение вправо (положительное направление X).
     */
    RIGHT(1, 0);

    /**
     * Изменение координаты X для этого направления.
     */
    public final int dx;

    /**
     * Изменение координаты Y для этого направления.
     */
    public final int dy;

    /**
     * Создает направление с заданными дельтами.
     *
     * @param dx изменение по X
     * @param dy изменение по Y
     */
    Direction(int dx, int dy) {
        this.dx = dx;
        this.dy = dy;
    }
}